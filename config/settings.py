# Cache
# One knob scales the whole app: set REDIS_URL in .env (e.g. redis://127.0.0.1:6379/1)
# and every cache.get_or_set / per-profile cache transparently moves to Redis — no code
# change. Without it, the in-process LocMem cache is used, so development and the current
# ~small deployment run with zero extra services. Django 6 ships the Redis backend
# natively (only the `redis` pip package is needed when REDIS_URL is set).
REDIS_URL = os.environ.get('REDIS_URL', '')
if REDIS_URL:
    CACHES = {
        'default': {
            'BACKEND': 'django.core.cache.backends.redis.RedisCache',
            'LOCATION': REDIS_URL,
        }
    }
else:
    CACHES = {
        'default': {
            'BACKEND': 'django.core.cache.backends.locmem.LocMemCache',
            'LOCATION': 'ilmmevasi-local',
        }
    }

# Sessions: by default Django reads the session row from the DB on EVERY request, which
# at scale is the single most repeated query in the app. With Redis configured we serve
# reads from cache and still persist to the DB, so nothing is lost if Redis is flushed.
# Without Redis the cache is per-process, so the plain DB backend stays correct.
if REDIS_URL:
    SESSION_ENGINE = 'django.contrib.sessions.backends.cached_db'



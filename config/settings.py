# SQLite serialises every write behind a single lock — it cannot survive real concurrency.
# Set DATABASE_URL (e.g. postgres://user:pass@host:5432/dbname) and the app runs on
# PostgreSQL with no code change. CONN_MAX_AGE keeps connections alive between requests
# instead of paying a new TCP+auth handshake on every single one; health checks drop
# connections that a proxy/DB restart has already killed.
